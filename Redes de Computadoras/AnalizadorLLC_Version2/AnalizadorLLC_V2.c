#include<stdio.h>

void Analizador(unsigned char Trama[], int n){
	int i;
	unsigned char S[][5]={"RR","RNR", "REJ", "SREJ"};
	unsigned char UC[][6]={"UI","SIM", "-", "SARM", "UP", "-", "-", "SABM", "DISC", "-", "-", "SARME", "-", "-", "-", "SABME", "SNRM", "-", "-", "RSET", "-", "-", "XID", "-", "-", "-", "SNRME"};
	unsigned char UR[][6]={"UI","RIM", "-", "DM", "-", "-", "-", "SABM", "RD", "-", "-", "-", "UA", "-", "-", "-", "-", "FRMR", "-", "-", "-", "-", "XID", "-", "-", "-", "-"};
	
	switch(n){
		case 33:
			printf("Trama No. A");
			break;
		case 34:
			printf("Trama No. B");
			break;
		case 35:
			printf("Trama No. C");
			break;
		default:
			printf("Trama No. %d", n+1);
			break;
	}
	
	printf("\nMAC destino: ");
	for(i=0; i<6; i++)
		printf("%x, ", Trama[i]);
	printf("\nMAC origen: ");
	for(i=6; i<12; i++)
		printf("%x, ", Trama[i]);
	
	if((Trama[12]<<8 | Trama[13]) < 1500){
		printf("\nTamanio: %d bytes", (Trama[12]<<8 | Trama[13]));
		printf("\n***LLC***");
		
		switch(Trama[16] & 3){
			case 0: //T-I
				printf("\nTrama-Informacion:\n\tN(s): %d\n\tN(r): %d", Trama[16]>>1, Trama[17]>>1);
				if(Trama[17] & 1){
					if(Trama[15] & 1)
						printf("\nf");
					else
						printf("\np");
				}
				break;
			case 1: //T-S
				printf("\nTrama-Supervision (%s):\n\tN(r): %d", S[(Trama[16]>>2) & 3], Trama[17]>>1);
				break;
			case 2: //T-I
				printf("\nTrama-Informacion:\n\tN(s): %d\n\tN(r): %d", Trama[16]>>1, Trama[17]>>1);
				if(Trama[17] & 1){
					if(Trama[15] & 1)
						printf("\nf");
					else
						printf("\np");
				}
				break;
			case 3: //T-U
				printf("\nTrama-No Numerada:");
				if(Trama[16] & 16){ //pf=1?
					if(Trama[15] & 1) //LSB SAPo=1, es respuesta
						printf("T-U (%s), f", UR[((Trama[16]>>2) & 3) | ((Trama[16]>>3) & 28)]);
					else //es comando
						printf("T-U (%s), p", UC[((Trama[16]>>2)&3) | ((Trama[16]>>3)&28)]);
				}
				break;
		}
	}else if((Trama[12]<<8 | Trama[13]) == 2048){
		printf("\nIP");
	}else if((Trama[12]<<8 | Trama[13]) == 2054){
		printf("\nARP");
	}else{
		printf("\nOtro");
	}
	printf("\n\n\n");
}

